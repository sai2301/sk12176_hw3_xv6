int
sys_fork(void)
{
  return fork();
}

int
sys_exit(void)
{
  exit();
  return 0;  // not reached
}

int
sys_wait(void)
{
  return wait();
}

int
sys_kill(void)
{
  int pid;

  if(argint(0, &pid) < 0)
    return -1;
  return kill(pid);
}

int
sys_getpid(void)
{
  return myproc()->pid;
}

int
sys_sbrk(void)
{
  int addr;
  int n;

  if(argint(0, &n) < 0)
    return -1;
  addr = myproc()->sz;
  if(growproc(n) < 0)
    return -1;
  return addr;
}

int
sys_sleep(void)
{
  int n;
  uint ticks0;

  if(argint(0, &n) < 0)
    return -1;
  acquire(&tickslock);
  ticks0 = ticks;
  while(ticks - ticks0 < n){
    if(myproc()->killed){
      release(&tickslock);
      return -1;
    }
    sleep(&ticks, &tickslock);
  }
  release(&tickslock);
  return 0;
}

// return how many clock tick interrupts have occurred
// since start.
int
sys_uptime(void)
{
  uint xticks;

  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}

// -------- HW3: nice(pid, value) --------
// We call into a proc.c helper so sysproc.c doesn't touch ptable directly.
extern int proc_setnice(int pid, int val);

int
sys_nice(void)
{
  int pid, val;

  if(argint(0, &pid) < 0 || argint(1, &val) < 0)
    return -1;

  // clamp to [0,4] for 5 levels (0 = highest priority)
  if(val < 0) val = 0;
  if(val > 4) val = 4;

 return setnice(pid, val); // returns old nice on success, -1 on failure
}
